<h2>About</h2>

<p>
	This is link reformatter (possibly a shortener).  You can use it to
	turn ugly links into either informative ones or short ones.  Instead
	of just making them smaller, we aim to make them gooder by allowing
	you to use a category and/or incorporate information from the page into
	the link -- e.g. title, headings, etc.
</p>

<p>
	To use, enter an ugly link into our page, perhaps select and category, and then choose between <em>short</em> or <em>descriptive</em>.  Short links look like this:
	<blockquote>
		<a href="/5glVDb" class="url">
			http://urlu.ms/5glVDb
		</a>
	</blockquote>
	 and descriptive links look like this; we pull the new link name from the page itself (this one has a category, too):
	<blockquote>
		<a href="/schools/University-of-Colorado-at-Boulder" class="url">
			http://urlu.ms/schools/University-of-Colorado-at-Boulder
		</a>
	</blockquote>

